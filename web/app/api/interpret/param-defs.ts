// ═══════════════════════════════════════════════════════════
//  Derived helpers (consumers call these instead of duplicating)
// ═══════════════════════════════════════════════════════════

/** Auto-generate PARAM_RULES from PARAM_DEFS */
export function getParserRules(): ParamRule[] {
  return PARAM_DEFS.flatMap(d => d.parserRules);
}

/** Extract {family, value} from a tag — unified paramFamily */
export function paramFamily(tag: string): ParamFamilyResult | null {
  for (const d of PARAM_DEFS) {
    // Skip non-threshold families (__txtr__, __mux__)
    if (d.family.startsWith('__')) continue;
    const m = tag.match(d.tagPattern);
    if (m) {
      // For Vin/Vout which may have negative values
      const val = parseFloat(m[1]);
      if (!Number.isNaN(val)) return { family: d.family, value: val };
    }
  }
  return null;
}

/** Families that support downgrade (>= comparison) */
export function getDowngradableFamilies(): Set<string> {
  return new Set(PARAM_DEFS.filter(d => d.downgradable).map(d => d.family));
}

/** Auto-generate VALID_PATTERNS for route.ts soft-audit */
export function getValidPatterns(): RegExp[] {
  return PARAM_DEFS.map(d => d.tagPattern);
}

/** Look up a ParamDef by family name */
export function getParamDef(family: string): ParamDef | undefined {
  return PARAM_DEFS.find(d => d.family === family);
}

/** Map from family → product token regex (lowercased) */
export function getProductTokenPattern(family: string): RegExp | undefined {
  const d = PARAM_DEFS.find(d => d.family === family);
  return d?.productTokenPattern ?? d?.tagPattern;
}
