/**
 * param-defs.ts — Single registry for all numerical parameter definitions.
 *
 * Architecture: Every numerical parameter (Mbps, kVrms, Vin, Vout, Iout, 通道, bit, 口)
 * is defined here ONCE.  Parser rules, family extraction, constraint comparison,
 * and validation patterns all derive from this file.
 *
 * Adding a new parameter: add ONE entry to PARAM_DEFS.
 */

// ── Utility ──
function fmtNum(value: number): string {
  return Number.isInteger(value) ? `${value}` : `${parseFloat(value.toFixed(3))}`;
}

// ── Types ──
export interface ParamRule {
  pattern: RegExp;
  extract: (m: RegExpMatchArray) => string[];
}

export interface ParamFamilyResult {
  family: string;
  value: number;
}

export interface ParamDef {
  /** 参数族名，用于 constraint 维度标注和 mustMeta */
  family: string;
  /** 比较方向：>= 需求值≤产品值即满足，<= 需求值≥产品值即满足，range 需求值在[min,max]内，exact 精确匹配 */
  compare: '>=' | '<=' | 'range' | 'exact';
  /** 是否可向下兼容（如 Mbps / 通道 / 端口） */
  downgradable: boolean;
  /** 从 tag 字符串反解出 family+value（用于 paramFamily） */
  tagPattern: RegExp;
  /** 用户 query → tag 的正则提取规则（可多条，覆盖不同表达方式） */
  parserRules: ParamRule[];
  /** 从产品 _features token 中提取数值的模式（默认 = tagPattern.toLowercase） */
  productTokenPattern?: RegExp;
  /** 注入 LLM prompt 的参数行为说明 */
  llmGuidance?: string;
}

// ═══════════════════════════════════════════════════════════
//  PARAM_DEFS — single source of truth
// ═══════════════════════════════════════════════════════════

